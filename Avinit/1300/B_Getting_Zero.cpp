signed main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int n;
    cin >> n;
    vector<int> vt(n);
    for (int i = 0; i < n; ++i)
    {
        cin >> vt[i];
    }
    map<int, int> mp;
    int limit = 32768;
    for (int i = 1; i <= limit; ++i)
    {
        int current = i;
        int two = 0;
        while ((current % 2 == 0) && current > 0)
        {
            two++;
            current /= 2;
        }
        mp[i] = two;
    }
    vector<int> answer(n);
    for (int i = 0; i < n; ++i)
    {
        if(vt[i]==0)
        {
            answer[i]=0;
        }
        else
        {
            answer[i]=15-mp[vt[i]];
        }
    }
    for (int i = 0; i < n; ++i)
    {
        for (int j = 0; j <= 15; ++j)
        {
            int current = vt[i] + j;
            if(current>limit)
            {
                break;
            }
            int operation = max(15 - mp[current], 0LL) + j;
            answer[i] = min(answer[i], operation);
        }
    }
    for (auto x : answer)
    {
        cout << x << " ";
    }
}