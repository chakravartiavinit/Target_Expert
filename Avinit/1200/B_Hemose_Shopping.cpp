signed main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int t;
    cin >> t;
    while (t--)
    {
        int n, k;
        cin >> n >> k;
        vector<int> vt(n);
        map<int, set<int, greater<int>>> mp;
        for (int i = 0; i < n; ++i)
        {
            cin >> vt[i];
            mp[vt[i]].insert(i);
        }
        vector<int> sorted_vt = vt;
        sort(sorted_vt.begin(), sorted_vt.end());
        if (sorted_vt == vt)
        {
            cout << "YES\n";
        }
        else
        {
            bool possible = true;
            int start = n - k;
            int end = k - 1;
            for (int i = start; i <= end; ++i)
            {
                if (sorted_vt[i] != vt[i])
                {
                    possible = false;
                    break;
                }
            }
            possible == true ? cout << "YES\n" : cout << "NO\n";
        }
    }
}