bool check(int i, int j, int n, int m)
{
    if (i < 0 || j < 0 || i >= n || j >= m)
    {
        return false;
    }
    return true;
}
bool Intersect(vector<string> &vt, int n, int m)
{
    for (int i = 0; i < n; ++i)
    {
        for (int j = 0; j < m; ++j)
        {
            if (check(i + 1, j, n, m) == true && check(i, j + 1, n, m) == true && check(i + 1, j + 1, n, m) == true)
            {
                if ((vt[i][j] - '0') + (vt[i + 1][j] - '0') + (vt[i][j + 1] - '0') + (vt[i + 1][j + 1] - '0') == 3)
                {
                    return true;
                }
            }
        }
    }
    return false;
}
