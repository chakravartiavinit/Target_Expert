signed main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        vector<int> a(n), b(n);
        for (int i = 0; i < n; ++i)
        {
            cin >> a[i];
        }
        for (int i = 0; i < n; ++i)
        {
            cin >> b[i];
        }
        vector<pair<int, int>> vp, answer;
        for (int i = 0; i < n; ++i)
        {
            vp.push_back({a[i], b[i]});
        }
        for (int i = 0; i < n; ++i)
        {
            pair<int, int> temp = vp[i];
            int changed_index = -1;
            for (int j = i + 1; j < n; ++j)
            {
                if (temp.first > vp[j].first)
                {
                    temp = vp[j];
                    changed_index = j;
                }
                else if (temp.first == vp[j].first)
                {
                    if (temp.second > vp[j].second)
                    {
                        temp = vp[j];
                        changed_index = j;
                    }
                }
            }
            if (changed_index != -1)
            {
                answer.push_back({i, changed_index});
                swap(vp[i], vp[changed_index]);
            }
        }
        bool exist = true;
        for (int i = 0; i + 1 < n; ++i)
        {
            if (vp[i].first > vp[i + 1].first || vp[i].second > vp[i + 1].second)
            {
                exist = false;
                break;
            }
        }
        if (exist == false)
        {
            cout << "-1\n";
        }
        else
        {
            cout << answer.size() << "\n";
            for (auto x : answer)
            {
                cout << x.first + 1 << " " << x.second + 1 << "\n";
            }
        }
    }
}