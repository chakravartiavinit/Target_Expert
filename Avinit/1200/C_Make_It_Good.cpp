bool Check(int index, vector<int> &vt)
{
    int i = index, j = vt.size() - 1;
    vector<int> temp;
    while (i <= j)
    {
        int delta = min(vt[i], vt[j]);
        temp.push_back(delta);
        if (delta == vt[i])
        {
            i++;
        }
        else
        {
            j--;
        }
    }
    for (int i = 0; i + 1 < temp.size(); ++i)
    {
        if (temp[i] > temp[i + 1])
        {
            return false;
        }
    }
    return true;
}
signed main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        vector<int> vt(n);
        for (int i = 0; i < n; ++i)
        {
            cin >> vt[i];
        }
        int low = 0, high = n - 1;
        int answer = n;
        while (low <= high)
        {
            int mid = (low + high) / 2;
            if (Check(mid, vt) == true)
            {
                answer = mid;
                high = mid - 1;
            }
            else
            {
                low = mid + 1;
            }
        }
        cout << answer<< "\n";
    }
}